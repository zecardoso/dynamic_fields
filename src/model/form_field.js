export class FormField {
    constructor(FormId, FieldId, Name, Caption, Hidden, RankOrder, Enabled) {
        this.FormId = FormId;
        this.FieldId = FieldId;
        this.Name = Name;
        this.Caption = Caption;
        this.Hidden = Hidden;
        this.RankOrder = RankOrder;
        this.Enabled = Enabled;
    }
}
